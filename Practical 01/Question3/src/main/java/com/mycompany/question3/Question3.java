
package com.mycompany.question3;
//forloop Example
public class Question3
{
    public static void main(String[] args)
    {
        for (int i = 0; i <= 4; i++)
        {
            System.out.println("Executing Loop " + i);
        }
    }
}
//whilel loop Example
public class Questionwhileloop
{
public static void main(String[] args)
{
        int i = 0;
        while (i <= 4) 
        {
            System.out.println("Executing Loop " + i);
            i++;
        }
}
}

